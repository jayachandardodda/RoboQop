  onSubmitBacktesting() {

    var portfolio_id = sessionStorage.getItem('portfolio_id');
    if (this.backTesting == true) {
      this.isCursorLoading = true;
      var req = {
        portfolio_id: portfolio_id,
        start_date: this.backtestingForm.get('start_time')?.value,
        end_date: this.backtestingForm.get('end_time')?.value,
      }
      this.holdingService.backTesting(req).subscribe({
        next: (result) => {
          this.isCursorLoading = false;
          this.holdingService.setBacktestData(result);
        },
        error: (error) => {
          console.log(error)
        },
        complete: () => {
          var data = this.holdingService.getBacktestData();
          console.log(data)
          for (var i = 0; i < data.graph_data.backtest.length; i++) {
            data.graph_data.backtest[i].splice(0, 1, new Date(data.graph_data.backtest[i][0]));
            data.graph_data.backtest[i][1] = Number(parseFloat(data.graph_data.backtest[i][1]).toFixed(2));
          }
          this.setLineChart(data.graph_data.backtest)
          this.setPieChart(data.graph_data.weights)
        }
      });
    }
  }

  optimise(form) {
    if (this.optimize.get('backend')?.value != null) {
      this.isCursorLoading = true
      var id = sessionStorage.getItem('portfolio_id');
      var backend = form.get('backend')?.value;
      var request = {};
      if (this.enableInvestorsView) {
        var investors_views = form.get('investors_views')?.value

        for (let i = 0; i < investors_views.length; i++) {
          const current_obj: InvestorsView = {}
          var outperforming_asset: string[] = [];
          var temp = investors_views[i].outperforming_asset
          temp.forEach((element) => {
            outperforming_asset.push(element.name);
          });
          current_obj.outperforming_asset = outperforming_asset;

          var underperforming_asset = []
          temp = investors_views[i].underperforming_asset
          temp.forEach((element) => {
            underperforming_asset.push(element.name);
          });
          current_obj.underperforming_asset = underperforming_asset;
          current_obj.by = investors_views[i].by;
          current_obj.confidence = investors_views[i].confidence;
          request[i] = current_obj;

        }
      }
      var cardinality = form.get('cardinality')?.value;
      var problem = 'Risk Return'
      var strategy = form.get('strategy')?.value
      if (form.get('targetSolution')?.value == 'Risk Returns') {
        problem = 'Risk Return'
      }
      else {
        problem = form.get('targetSolution')?.value;
      }
      //this.myForm['controls'].child['controls'].id.valid
      var start_time = form.get('backTesting').get('start_time')?.value;
      var end_time = form.get('backTesting').get('end_time')?.value
      var lam = (form.get('lambdaInput')?.value) / 100
      var target_risk = (form.get('targetRiskInput')?.value)
      var target_return = (form.get('targetReturnsInput')?.value)
      console.log(start_time, end_time, lam)
      var req = {

        "portfolio_id": id,
        "backend": backend,
        "strategy": strategy,
        "modelportfolio": 1,
        "lb": 0,
        "ub": 1,
        "lam": lam,
        "problem": problem,
        "target_risk": target_risk,
        "target_returns": target_return,
        "cardinality": cardinality,
        "start_date": start_time,
        "end_date": end_time,
        "investors_views": request

      }
      console.log(req)

      this.holdingService.optimise(req).subscribe({
        next: (response: any) => {
          var data = response;
          for (var i = 0; i < data.graph_data.graph.length; i++) {
            data.graph_data.graph[i].splice(0, 1, new Date(data.graph_data.graph[i][0]));
            data.graph_data.graph[i][1] = Number(parseFloat(data.graph_data.graph[i][1]).toFixed(2));
          }
          console.log(response);
          this.efficientFrontier = true;
          this.setLineChart(data.graph_data.graph);
          this.setPieChart(data.graph_data.weights);
          this.setScatterChart(data.graph_data.efficient_frontier);
          this.optimize_btn = 1;
          this.isCursorLoading = false
        },
        error: (error) => {
          console.log(error)
        }
      })
    }
    else if (this.optimize.get('backend')?.value == null || this.optimize.get('backend')?.value == undefined) {
      this.optimizeError = true
    }
  }

